#include "libft.h"

int	ft_atoi(const char *str)
{
	int		n;
	int		s;

	n = 0;
	s = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
	{
		str++;
	}
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			s++;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		n *= 10;
		n += *str - 48;
		str++;
	}
	if (!(s % 2))
		return (n);
	return (-n);
}
